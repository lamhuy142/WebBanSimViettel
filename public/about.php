<script>
	// Function to handle click event on each tab link
	function handleTabClick(tabId, contentId) {
		// Remove 'active' class from all tab links
		document.querySelectorAll('.nav-item.nav-link').forEach(function(link) {
			link.classList.remove('active');
		});

		// Add 'active' class to the clicked tab link
		document.getElementById(tabId).classList.add('active');

		// Hide all tab contents
		document.querySelectorAll('.tab-pane').forEach(function(content) {
			content.style.display = 'none';
		});

		// Show the content of the clicked tab
		document.getElementById(contentId).style.display = 'block';
	}

	// Add event listeners to tab links
	document.getElementById('muc1Link').addEventListener('click', function() {
		handleTabClick('muc1Link', 'muc1');
	});

	document.getElementById('muc2Link').addEventListener('click', function() {
		handleTabClick('muc2Link', 'muc2');
	});

	document.getElementById('muc3Link').addEventListener('click', function() {
		handleTabClick('muc3Link', 'muc3');
	});
	document.getElementById('muc4Link').addEventListener('click', function() {
		handleTabClick('muc4Link', 'muc4');
